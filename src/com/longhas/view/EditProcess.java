    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try{

           int size = Integer.parseInt(memorySizeSpinner.getValue().toString());

           if(processDescription.getText().isEmpty()){
               JOptionPane.showMessageDialog(this, "Empty process description", "Edit Process", JOptionPane.ERROR_MESSAGE);
           }else if(size<=0){
               JOptionPane.showMessageDialog(this, "Invalid quanta", "Edit Process", JOptionPane.ERROR_MESSAGE);
           }else{

               updatedProcess.setProcessName(type.getSelectedItem().toString() + " : " + processDescription.getText());
               updatedProcess.setMemorySize(size);
               this.setVisible(false);

            }

       }catch(NumberFormatException e){
               JOptionPane.showMessageDialog(this, "Invalid integer : " + e.getMessage(), "Edit Process", JOptionPane.ERROR_MESSAGE);
       }

    }//GEN-LAST:event_jButton1ActionPerformed

