    private void displayDetails(){
        if(updatedProcess!=null){
            try{
                StringTokenizer str = new StringTokenizer(updatedProcess.getProcessName(), " : ");
                type.setSelectedItem((Object)str.nextToken());
                if(str.hasMoreTokens()){
                    processDescription.setText(str.nextToken());
                }
                memorySizeSpinner.setValue((Object)updatedProcess.getMemorySize());
            }catch(NoSuchElementException e){
                JOptionPane.showMessageDialog(this, "Error in parsing process description " + e.getMessage(), "Display Details", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

   private void setPriorityOptionsData(){
        if(memorySize!=0){
                memorySizeSpinner.setModel(new javax.swing.SpinnerNumberModel(1, 1, memorySize, 1));
        }else{
                memorySizeSpinner.setModel(new javax.swing.SpinnerNumberModel(1, 1, 1, 1));
        }
   }

